/* =========================================================
   utils/arabicNormalize.js
   توحيد النص العربي للبحث الذكي:
   - توحيد الهمزات (أ إ آ ٱ → ا)
   - توحيد التاء المربوطة (ة → ه)
   - توحيد الألف المقصورة (ى → ي)
   - حذف الحركات والتشكيل
   - حذف التطويل
   بهذا: "احمد" = "أحمد" = "أحمد" عند البحث
   ========================================================= */

function normalizeArabic(text) {
  if (!text) return "";
  return text
    .replace(/[أإآٱ]/g, "ا")           // الهمزات → ألف عادية
    .replace(/ة/g, "ه")                // تاء مربوطة → هاء
    .replace(/ى/g, "ي")                // ألف مقصورة → ياء
    .replace(/[ً-ٰٟ]/g, "") // إزالة التشكيل والحركات
    .replace(/ـ/g, "")                 // إزالة التطويل
    .trim()
    .toLowerCase();
}

module.exports = { normalizeArabic };
